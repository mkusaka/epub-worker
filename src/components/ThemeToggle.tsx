export function ThemeToggle() {
  const { theme, toggleTheme } = useTheme();

  const icon =
    theme === "light" ? <SunIcon /> : theme === "dark" ? <MoonIcon /> : <SystemIcon />;

  const label =
    theme === "light" ? "Light" : theme === "dark" ? "Dark" : "System";

  return (
    <Button
      variant="ghost"
      size="icon"
      onClick={toggleTheme}
      aria-label={`Theme: ${label}`}
      title={`Theme: ${label}`}
    >
      {icon}
    </Button>
  );
}
